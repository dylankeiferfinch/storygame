package zzz_trash;

public class InputPartTag
{
	private String name;
	private InputPart value;
	
	public InputPartTag(String name, InputPart value)
	{
		this.name = name;
		this.value = value;
	}
	
	public String getName()
	{
		return name;
	}
	
	public InputPart getValue()
	{
		return value;
	}
}
